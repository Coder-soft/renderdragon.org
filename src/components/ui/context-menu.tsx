// Add effect to prevent layout shift when opening context menu
const PreventLayoutShift = () => {
  React.useEffect(() => {
    const html = document.documentElement;
    const scrollbarWidth = window.innerWidth - html.clientWidth;
    
    if (scrollbarWidth > 0) {
      const originalPaddingRight = getComputedStyle(html).getPropertyValue('padding-right');
      
      html.style.paddingRight = `${scrollbarWidth}px`;
      html.classList.add('overflow-hidden');
      
      return () => {
        html.style.paddingRight = originalPaddingRight;
        html.classList.remove('overflow-hidden');
      };
    }
  }, []);
  
  return null;
}

const ContextMenu = React.forwardRef<
  React.ElementRef<typeof ContextMenuPrimitive.Root>,
  React.ComponentPropsWithoutRef<typeof ContextMenuPrimitive.Root> & { 
    // Add our custom property to extend the type
    preventLayoutShift?: boolean 
  }
>(({ children, preventLayoutShift, ...props }, ref) => (
  <ContextMenuPrimitive.Root {...props}>
    {preventLayoutShift && <PreventLayoutShift />}
    {children}
  </ContextMenuPrimitive.Root>
))
ContextMenu.displayName = ContextMenuPrimitive.Root.displayName

